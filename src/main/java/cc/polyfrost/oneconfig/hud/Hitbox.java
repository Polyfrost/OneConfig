package cc.polyfrost.oneconfig.hud;

public class Hitbox {
    public float x;
    public float y;
    public float width;
    public float height;

    public Hitbox(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
