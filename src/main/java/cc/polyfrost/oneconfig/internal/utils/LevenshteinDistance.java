package cc.polyfrost.oneconfig.internal.utils;

import java.util.Locale;

/**
 * Based on <a href="https://www.baeldung.com/java-levenshtein-distance">https://www.baeldung.com/java-levenshtein-distance</a>
 */
public class LevenshteinDistance {

    public static boolean isSimilar(String s1, String s2) {
        return distance(s1.toLowerCase(Locale.ENGLISH).substring(0, Math.min(s1.length(), s2.length())), s2.toLowerCase(Locale.ENGLISH)) < 4;
    }

    public static int distance(String x, String y) {
        int[][] dp = new int[x.length() + 1][y.length() + 1];

        for (int i = 0; i <= x.length(); i++) {
            for (int j = 0; j <= y.length(); j++) {
                if (i == 0) {
                    dp[i][j] = j;
                } else if (j == 0) {
                    dp[i][j] = i;
                } else {
                    dp[i][j] = min(dp[i - 1][j - 1]
                                    + costOfSubstitution(x.charAt(i - 1), y.charAt(j - 1)),
                            dp[i - 1][j] + 1,
                            dp[i][j - 1] + 1);
                }
            }
        }

        return dp[x.length()][y.length()];
    }

    private static int costOfSubstitution(char a, char b) {
        return a == b ? 0 : 1;
    }

    private static int min(int a, int b, int c) {
        return Math.min(a, Math.min(b, c));
    }
}
